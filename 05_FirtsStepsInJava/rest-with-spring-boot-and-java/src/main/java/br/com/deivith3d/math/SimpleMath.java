package br.com.deivith3d.math;


public class SimpleMath {

	public Double sum(Double numberOne,Double numberTwo) {

		return numberOne+numberTwo;
	}
	
	public Double subt(Double numberOne, Double numberTwo){
		
		return numberOne-numberTwo;
	}
	
	public Double mult(Double numberOne,Double numberTwo){

		return numberOne*numberTwo;
	}
	
	public Double divs(Double numberOne,Double numberTwo){

		return numberOne/numberTwo;
	}
	
	public Double med(Double numberOne,Double numberTwo){
		
		return (numberOne+numberTwo)/2;
	}
	
	public Double raiz(Double numberOne){

		return Math.sqrt(numberOne);
	}
	
}
